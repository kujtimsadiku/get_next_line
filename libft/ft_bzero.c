#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*dst;
	size_t	i;

	i = 0;
	dst = (char *)s;
	if (n == 0)
		return ;
	while (i < n)
	{
		dst[i] = '\0';
		i++;
	}
}

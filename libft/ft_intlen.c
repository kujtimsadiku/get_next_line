#include "libft.h"

int	ft_intlen(long int nb)
{
	int	len;

	len = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
	{
		len++;
		if (nb == -2147483648)
			nb = (nb + 1) * -1;
		else
			nb *= -1;
	}
	while (nb > 0)
	{
		len++;
		nb = nb / 10;
	}
	return (len);
}
